package com.leondesilva.jcor.demo.Demo1;

/**
 * Class to represent the input required for the demo
 *
 */
class Input {
    private int count;

    /**
     * Constructor to instantiate input
     *
     * @param count the count
     */
    Input(int count) {
        this.count = count;
    }

    /**
     * Method to get the count
     *
     * @return the count
     */
    int getCount() {
        return count;
    }
}
